/// üì° What chain are your contracts deployed to?
const targetNetwork = NETWORKS["localhost"]; // <------- select your target frontend network (localhost, rinkeby, xdai, mainnet)

// const poolServerUrl = "https://backend.multisig.holdings:49832/";
const poolServerUrl = "http://localhost:49832/";

// üò¨ Sorry for all the console logging
const DEBUG = true;

// üõ∞ providers
if (DEBUG) console.log("üì° Connecting to Mainnet Ethereum");
// const mainnetProvider = getDefaultProvider("mainnet", { infura: INFURA_ID, etherscan: ETHERSCAN_KEY, quorum: 1 });
// const mainnetProvider = new InfuraProvider("mainnet",INFURA_ID);
//
// attempt to connect to our own scaffold eth rpc and if that fails fall back to infura...
const scaffoldEthProvider = new JsonRpcProvider("https://rpc.scaffoldeth.io:48544");
const mainnetInfura = new JsonRpcProvider("https://mainnet.infura.io/v3/" + INFURA_ID);
// ( ‚ö†Ô∏è Getting "failed to meet quorum" errors? Check your INFURA_I

// üè† Your local provider is usually pointed at your local blockchain
const localProviderUrl = targetNetwork.rpcUrl;
// as you deploy to other networks you can set REACT_APP_PROVIDER=https://dai.poa.network in packages/react-app/.env
const localProviderUrlFromEnv = process.env.REACT_APP_PROVIDER ? process.env.REACT_APP_PROVIDER : localProviderUrl;
if (DEBUG) console.log("üè† Connecting to provider:", localProviderUrlFromEnv);
const localProvider = new StaticJsonRpcProvider(localProviderUrlFromEnv);

// üî≠ block explorer URL
const blockExplorer = targetNetwork.blockExplorer;

function App(props) {
  const location = useLocation();
  const mainnetProvider = scaffoldEthProvider && scaffoldEthProvider._network ? scaffoldEthProvider : mainnetInfura;

  const [injectedProvider, setInjectedProvider] = useState();
  /* üíµ This hook will get the price of ETH from ü¶Ñ Uniswap: */
  const price = useExchangePrice(targetNetwork, mainnetProvider);

  /* üî• This hook will get the price of Gas from ‚õΩÔ∏è EtherGasStation */
  const gasPrice = useGasPrice(targetNetwork, "fast");
  // Use your injected provider from ü¶ä Metamask or if you don't have it then instantly generate a üî• burner wallet.
  const userProvider = useUserProvider(injectedProvider, localProvider);
  const address = useUserAddress(userProvider);

  // You can warn the user if you would like them to be on a specific network
  let localChainId = localProvider && localProvider._network && localProvider._network.chainId;
  let selectedChainId = userProvider && userProvider._network && userProvider._network.chainId;

  // For more hooks, check out üîóeth-hooks at: https://www.npmjs.com/package/eth-hooks

  // The transactor wraps transactions and provides notificiations
  const tx = Transactor(userProvider, gasPrice);

  // Faucet Tx can be used to send funds from the faucet
  const faucetTx = Transactor(localProvider, gasPrice);

  // üèó scaffold-eth is full of handy hooks like this one to get your balance:
  const yourLocalBalance = useBalance(localProvider, address);

  // Just plug in different üõ∞ providers to get your balance on different chains:
  const yourMainnetBalance = useBalance(mainnetProvider, address);

  // Load in your local üìù contract and read a value from it:
  const readContracts = useContractLoader(localProvider);

  // If you want to make üîê write transactions to your contracts, use the userProvider:
  const writeContracts = useContractLoader(userProvider);

  const walletContractName = "MetaMultiSigWallet";
  const tokenContractName = "WalletGovToken";

  //üìü Listen for ownership events
  const tokenTransferEvents = useEventListener(readContracts, tokenContractName, "Transfer", localProvider, 1);

  //üìü Listen for broadcast events
  const executeTransactionEvents = useEventListener(
    readContracts,
    walletContractName,
    "ExecuteTransaction",
    localProvider,
    1,
  );
  if (DEBUG) console.log("üìü executeTransactionEvents:", executeTransactionEvents);

  // keep track of a variable from the contract in the local React state:
  const hasWeight = useContractReader(writeContracts, walletContractName, "hasWeight", []);
  if (DEBUG) console.log("ü§ó hasWeight (" + address + "):", hasWeight);

  const tokenBalance = useContractReader(writeContracts, tokenContractName, "balanceOf", [address]);

  // keep track of a variable from the contract in the local React state:
  const nonce = useContractReader(readContracts, walletContractName, "nonce");
  if (DEBUG) console.log("# nonce:", nonce);

  //üìü Listen for broadcast events
  const ownerEvents = useEventListener(readContracts, walletContractName, "Owner", localProvider, 1);
  if (DEBUG) console.log("üìü ownerEvents:", ownerEvents);

  // If you want to bring in the mainnet DAI contract it would look like:
  const mainnetDAIContract = useExternalContractLoader(mainnetProvider, DAI_ADDRESS, DAI_ABI);

  // If you want to call a function on a new block
  useOnBlock(mainnetProvider, () => {
    console.log(`‚õì A new mainnet block is here: ${mainnetProvider._lastBlockNumber}`);
  });

  // Then read your DAI balance like:
  const myMainnetDAIBalance = useContractReader({ DAI: mainnetDAIContract }, "DAI", "balanceOf", [
    "0x34aA3F359A9D614239015126635CE7732c18fDF3",
  ]);

  // keep track of a variable from the contract in the local React state:
  const purpose = useContractReader(readContracts, "YourContract", "purpose");

  //üìü Listen for broadcast events
  const setPurposeEvents = useEventListener(readContracts, "YourContract", "SetPurpose", localProvider, 1);

  /*
  const addressFromENS = useResolveName(mainnetProvider, "austingriffith.eth");
  console.log("üè∑ Resolved austingriffith.eth as:",addressFromENS)
  */

  let networkDisplay = "";
  if (localChainId && selectedChainId && localChainId != selectedChainId) {
    networkDisplay = (
      <div style={{ zIndex: 2, position: "absolute", right: 0, top: 60, padding: 16 }}>
        <Alert
          message={"‚ö†Ô∏è Wrong Network"}
          description={
            <div>
              You have <b>{NETWORK(selectedChainId).name}</b> selected and you need to be on{" "}
              <b>{NETWORK(localChainId).name}</b>.
            </div>
          }
          type="error"
          closable={false}
        />
      </div>
    );
  } else {
    networkDisplay = (
      <div style={{ zIndex: -1, position: "absolute", right: 154, top: 28, padding: 16, color: targetNetwork.color }}>
        {targetNetwork.name}
      </div>
    );
  }

  const quorumPerMillion = useContractReader(readContracts, walletContractName, "quorumPerMillion");
  if (DEBUG) console.log("‚ú≥Ô∏è quorumPerMillion:", quorumPerMillion);

  //event OpenStream( address indexed to, uint256 amount, uint256 frequency );
  const openStreamEvents = useEventListener(readContracts, walletContractName, "OpenStream", localProvider, 1);
  if (DEBUG) console.log("üìü openStreamEvents:", openStreamEvents);

  const withdrawStreamEvents = useEventListener(readContracts, walletContractName, "Withdraw", localProvider, 1);
  if (DEBUG) console.log("üìü withdrawStreamEvents:", withdrawStreamEvents);

  const loadWeb3Modal = useCallback(async () => {
    const provider = await web3Modal.connect();
    setInjectedProvider(new Web3Provider(provider));
  }, [setInjectedProvider]);

  useEffect(() => {
    if (web3Modal.cachedProvider) loadWeb3Modal();
  }, [loadWeb3Modal]);

  let faucetHint = "";
  const faucetAvailable = localProvider && localProvider.connection && targetNetwork.name == "localhost";

  const [faucetClicked, setFaucetClicked] = useState(false);
  if (
    !faucetClicked &&
    localProvider &&
    localProvider._network &&
    localProvider._network.chainId == 31337 &&
    yourLocalBalance &&
    formatEther(yourLocalBalance) <= 0
  ) {
    faucetHint = (
      <div style={{ padding: 16 }}>
        <Button
          type={"primary"}
          onClick={() => {
            faucetTx({
              to: address,
              value: parseEther("1"),
            });
            setFaucetClicked(true);
          }}
        >
          üí∞ Grab funds from the faucet ‚õΩÔ∏è
        </Button>
      </div>
    );
  }

  return (
    <div className="App">
      {/* ‚úèÔ∏è Edit the header and change the title to your project name */}
      <Header />
      {networkDisplay}
      <Menu style={{ textAlign: "center" }} selectedKeys={[location.pathname]} mode="horizontal">
        <Menu.Item key="/">
          <Link to="/">Home</Link>
        </Menu.Item>
        <Menu.Item key="/transactions">
          <Link to="/transactions">Transactions</Link>
        </Menu.Item>
        <Menu.Item key="/debug">
          <Link to="/debug">Debug</Link>
        </Menu.Item>
      </Menu>

      <Switch>
        <Route exact path="/">
          <FrontPage
            executeTransactionEvents={executeTransactionEvents}
            userAddress={address}
            tokenTransferEvents={tokenTransferEvents}
            walletContractName={walletContractName}
            tokenContractName={tokenContractName}
            localProvider={localProvider}
            readContracts={readContracts}
            price={price}
            tx={tx}
            mainnetProvider={mainnetProvider}
            blockExplorer={blockExplorer}
            writeContracts={writeContracts}
            address={address}
            tokenBalance={tokenBalance}
            quorumPerMillion={quorumPerMillion}
          />
        </Route>
        <Route path="/transactions">
          <CreateTransaction
            poolServerUrl={poolServerUrl}
            walletContractName={walletContractName}
            quorumPerMillion={quorumPerMillion}
            blockExplorer={blockExplorer}
            address={address}
            userProvider={userProvider}
            mainnetProvider={mainnetProvider}
            nonce={nonce}
            tokenContractName={tokenContractName}
            localProvider={localProvider}
            yourLocalBalance={yourLocalBalance}
            price={price}
            tx={tx}
            writeContracts={writeContracts}
            readContracts={readContracts}
          />
        </Route>
        <Route path="/debug">
          <Contract
            name="WalletGovToken"
            signer={userProvider.getSigner()}
            provider={localProvider}
            address={readContracts && readContracts.WalletGovToken ? readContracts.WalletGovToken.address : ""}
            blockExplorer={blockExplorer}
          />
          <Contract
            name="MetaMultiSigWallet"
            signer={userProvider.getSigner()}
            provider={localProvider}
            address={address}
            blockExplorer={blockExplorer}
            purpose={purpose}
            setPurposeEvents={setPurposeEvents}
          />
        </Route>
        <Route path="/mainnetdai">
          <Contract
            name="DAI"
            customContract={mainnetDAIContract}
            signer={userProvider.getSigner()}
            provider={mainnetProvider}
            address={address}
            blockExplorer={"https://etherscan.io/"}
          />
        </Route>
        <Route path="/subgraph">
          <Subgraph
            subgraphUri={props.subgraphUri}
            tx={tx}
            writeContracts={writeContracts}
            mainnetProvider={mainnetProvider}
          />
        </Route>
      </Switch>

      <ThemeSwitch />

      {/* üë®‚Äçüíº Your account is in the top right with a wallet at connect options */}
      <div style={{ position: "fixed", textAlign: "right", right: 0, top: 0, padding: 10 }}>
        <Account
          address={address}
          localProvider={localProvider}
          userProvider={userProvider}
          mainnetProvider={mainnetProvider}
          price={price}
          web3Modal={web3Modal}
          loadWeb3Modal={loadWeb3Modal}
          logoutOfWeb3Modal={logoutOfWeb3Modal}
          blockExplorer={blockExplorer}
        />
        {faucetHint}
      </div>

      {/* üó∫ Extra UI like gas price, eth price, faucet, and support: */}
      <div style={{ position: "fixed", textAlign: "left", left: 0, bottom: 20, padding: 10 }}>
        <Row align="middle" gutter={[4, 4]}>
          <Col span={8}>
            <Ramp price={price} address={address} networks={NETWORKS} />
          </Col>

          <Col span={8} style={{ textAlign: "center", opacity: 0.8 }}>
            <GasGauge gasPrice={gasPrice} />
          </Col>
          <Col span={8} style={{ textAlign: "center", opacity: 1 }}>
            <Button
              onClick={() => {
                window.open("https://t.me/joinchat/KByvmRe5wkR-8F_zz6AjpA");
              }}
              size="large"
              shape="round"
            >
              <span style={{ marginRight: 8 }} role="img" aria-label="support">
                üí¨
              </span>
              Support
            </Button>
          </Col>
        </Row>

        <Row align="middle" gutter={[4, 4]}>
          <Col span={24}>
            {
              /*  if the local provider has a signer, let's show the faucet:  */
              faucetAvailable ? (
                <Faucet localProvider={localProvider} price={price} ensProvider={mainnetProvider} />
              ) : (
                ""
              )
            }
          </Col>
        </Row>
      </div>
    </div>
  );
}

/*
  Web3 modal helps us "connect" external wallets:
*/
const web3Modal = new Web3Modal({
  // network: "mainnet", // optional
  cacheProvider: true, // optional
  providerOptions: {
    walletconnect: {
      package: WalletConnectProvider, // required
      options: {
        infuraId: INFURA_ID,
      },
    },
  },
});

const logoutOfWeb3Modal = async () => {
  await web3Modal.clearCachedProvider();
  setTimeout(() => {
    window.location.reload();
  }, 1);
};

window.ethereum &&
  window.ethereum.on("chainChanged", chainId => {
    web3Modal.cachedProvider &&
      setTimeout(() => {
        window.location.reload();
      }, 1);
  });

window.ethereum &&
  window.ethereum.on("accountsChanged", accounts => {
    web3Modal.cachedProvider &&
      setTimeout(() => {
        window.location.reload();
      }, 1);
  });

export default App;
